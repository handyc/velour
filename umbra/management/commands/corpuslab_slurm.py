"""Generate a Slurm submission script for running corpuslab_run on ALICE.

Writes the script to stdout (or --output PATH).  Defaults to Leiden
ALICE's standard session shape: 20 CPUs × 4 h on the cpu-medium
partition.  Concrete's HPX scales across cores within one process,
so a single Slurm job on 20 cores is already a real speed-up over a
laptop without needing array-job parallelism.

Use:

    venv/bin/python manage.py corpuslab_slurm <session-slug> \\
        --output run.sh

…then on ALICE:

    sbatch run.sh

The generated script assumes the velour repo is checked out at the
same path on the cluster, with a venv at venv/ — adjust the
template via --repo-dir if your layout differs.
"""
import os
import textwrap

from django.core.management.base import BaseCommand, CommandError

from umbra.models import CorpusLabSession


SCRIPT_TEMPLATE = """\
#!/bin/bash
#SBATCH --job-name=umbra-{slug}
#SBATCH --partition={partition}
#SBATCH --time={hours}:00:00
#SBATCH --cpus-per-task={cpus}
#SBATCH --mem={mem_gb}G
#SBATCH --output=umbra-{slug}-%j.out
#SBATCH --error=umbra-{slug}-%j.err

# Generated by `manage.py corpuslab_slurm` for CorpusLabSession
# {slug!r} ({name}).  See umbra/corpuslab.py for the pipeline.

set -euo pipefail

cd {repo_dir}

# HPX scales across cores within one Concrete circuit invocation.
# Setting OMP/HPX thread counts so the runtime doesn't fight Slurm's
# affinity assignment.
export OMP_NUM_THREADS=$SLURM_CPUS_PER_TASK
export HPX_NUM_THREADS=$SLURM_CPUS_PER_TASK

venv/bin/python manage.py corpuslab_run {slug}
"""


class Command(BaseCommand):
    help = ('Generate a Slurm submission script for running a '
            'CorpusLabSession on ALICE.')

    def add_arguments(self, parser):
        parser.add_argument('slug', help='session slug')
        parser.add_argument('--output', '-o', default=None,
            help='write to this path instead of stdout')
        parser.add_argument('--partition', default='cpu-medium',
            help='Slurm partition (default cpu-medium)')
        parser.add_argument('--cpus', type=int, default=20,
            help='cores per task (default 20 — ALICE\'s standard slice)')
        parser.add_argument('--hours', type=int, default=4,
            help='wall hours (default 4)')
        parser.add_argument('--mem-gb', type=int, default=16,
            help='memory per node, GiB (default 16)')
        parser.add_argument('--repo-dir',
            default='/home/$USER/velour',
            help='repo path on the cluster')

    def handle(self, *args, **opts):
        try:
            s = CorpusLabSession.objects.get(slug=opts['slug'])
        except CorpusLabSession.DoesNotExist:
            raise CommandError(f'no session with slug {opts["slug"]!r}')

        script = SCRIPT_TEMPLATE.format(
            slug      = s.slug,
            name      = s.name or s.slug,
            partition = opts['partition'],
            cpus      = opts['cpus'],
            hours     = opts['hours'],
            mem_gb    = opts['mem_gb'],
            repo_dir  = opts['repo_dir'],
        )

        if opts['output']:
            with open(opts['output'], 'w') as fp:
                fp.write(script)
            os.chmod(opts['output'], 0o755)
            self.stdout.write(self.style.SUCCESS(
                f'wrote {opts["output"]}  ({len(script)} bytes)'))
        else:
            self.stdout.write(script)
