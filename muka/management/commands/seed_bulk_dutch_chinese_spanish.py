SEEDS = {
    'dutch':    DUTCH,
    'mandarin': MANDARIN,
    'spanish':  SPANISH,
}


class Command(BaseCommand):
    help = ('Bulk seed: 45 additional sentences each for Dutch, '
            'Mandarin, and Spanish (bringing each to 50 trees '
            'total). Additive вҖ” safe to re-run. Run '
            'seed_world_languages first so the Language rows exist.')

    def handle(self, *args, **opts):
        made_sent, updated_sent, bad_trees, missing_lang = 0, 0, 0, 0

        for lang_slug, sentences in SEEDS.items():
            try:
                lang = Language.objects.get(slug=lang_slug)
            except Language.DoesNotExist:
                missing_lang += 1
                self.stderr.write(self.style.WARNING(
                    f'! Language {lang_slug!r} not found вҖ” run '
                    f'seed_world_languages first. Skipping '
                    f'{len(sentences)} sentences.'))
                continue

            self.stdout.write(
                f'В· {lang_slug} ({lang.english_name}) вҖ” '
                f'{len(sentences)} to ingest')
            with transaction.atomic():
                for row in sentences:
                    try:
                        parse_bracket(row['tree_bracket'])
                    except Exception as e:
                        bad_trees += 1
                        self.stderr.write(self.style.ERROR(
                            f'  ! {row["slug"]}: bad tree ({e})'))
                        continue
                    defaults = {**{k: v for k, v in row.items()
                                   if k != 'slug'},
                                'language': lang}
                    _, was_new = Sentence.objects.update_or_create(
                        slug=row['slug'], defaults=defaults)
                    if was_new:
                        made_sent += 1
                    else:
                        updated_sent += 1

        msg = (f'Sentences: +{made_sent} new, ~{updated_sent} updated, '
               f'{bad_trees} bad trees')
        if missing_lang:
            msg += f', {missing_lang} languages missing'
        self.stdout.write(self.style.SUCCESS(msg + '.'))
