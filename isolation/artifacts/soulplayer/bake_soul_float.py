#!/usr/bin/env python3
"""bake_soul_float.py — emit a header with float32 weights from a
trained .pt + tokenizer.json.  Used by officesoulflt.c (float
inference, no quantization).

Usage:
  python3 bake_soul_float.py weights.pt tokenizer.json soul_data_float.h
"""
import json, struct, sys, math
from pathlib import Path
import torch

VS, ED, NH, HD, FF, NL, SL = 128, 32, 4, 8, 64, 2, 64


def main():
    pt_path  = Path(sys.argv[1])
    tok_path = Path(sys.argv[2])
    out_path = Path(sys.argv[3])

    sd = torch.load(pt_path, map_location='cpu', weights_only=False)
    tok = json.loads(tok_path.read_text())

    # Vocab packing — identical to gen_soul_data.py.
    vocab = tok["vocab"]
    id_to_str = [""] * VS
    for tokstr, tid in vocab.items():
        if 0 <= tid < VS:
            id_to_str[tid] = tokstr
    blob = bytearray()
    offsets = []
    lengths = []
    for s in id_to_str:
        offsets.append(len(blob))
        b = s.encode('utf-8')
        lengths.append(len(b))
        blob.extend(b)
        blob.append(0)
    str_to_id = {s: i for i, s in enumerate(id_to_str) if s}
    merges = []
    for a, b in tok["merges"]:
        merged = a + b
        if a in str_to_id and b in str_to_id and merged in str_to_id:
            merges.append((str_to_id[a], str_to_id[b], str_to_id[merged]))

    out = []
    w = out.append
    w("/* AUTO-GENERATED by bake_soul_float.py — do not edit. */\n")
    w("#ifndef SOUL_DATA_FLOAT_H\n#define SOUL_DATA_FLOAT_H\n\n")
    w(f"#define VS  {VS}\n#define ED  {ED}\n#define NH  {NH}\n")
    w(f"#define HD  {HD}\n#define FF  {FF}\n#define NL  {NL}\n#define SL  {SL}\n\n")
    w(f"#define VOCAB_STR_BLOB_LEN {len(blob)}\n")
    w(f"#define MERGES_N      {len(merges)}\n\n")

    def emit_float(name, t, dims):
        flat = t.reshape(-1).tolist()
        size = math.prod(dims)
        assert len(flat) == size, f"{name}: {len(flat)} ≠ {dims}"
        type_decl = "static const float " + name + ''.join(f"[{d}]" for d in dims)
        w(f"{type_decl} = {{\n")
        for i in range(0, size, 8):
            chunk = flat[i:i+8]
            w("    " + ", ".join(f"{v:+.8e}f" for v in chunk) + ",\n")
        w("};\n\n")

    emit_float("W_TE", sd['te.weight'],          [VS, ED])
    emit_float("W_PE", sd['pe.weight'],          [SL, ED])
    for L in range(NL):
        emit_float(f"W_N1_{L}",       sd[f'layers.{L}.n1.w'],            [ED])
        emit_float(f"W_QW_{L}",       sd[f'layers.{L}.att.q.weight'],    [ED, ED])
        emit_float(f"W_KW_{L}",       sd[f'layers.{L}.att.k.weight'],    [ED, ED])
        emit_float(f"W_VW_{L}",       sd[f'layers.{L}.att.v.weight'],    [ED, ED])
        emit_float(f"W_PROJ_{L}",     sd[f'layers.{L}.att.proj.weight'], [ED, ED])
        emit_float(f"W_N2_{L}",       sd[f'layers.{L}.n2.w'],            [ED])
        emit_float(f"W_FC1W_{L}",     sd[f'layers.{L}.ffn.fc1.weight'],  [FF, ED])
        emit_float(f"W_FC1B_{L}",     sd[f'layers.{L}.ffn.fc1.bias'],    [FF])
        emit_float(f"W_FC2W_{L}",     sd[f'layers.{L}.ffn.fc2.weight'],  [ED, FF])
        emit_float(f"W_FC2B_{L}",     sd[f'layers.{L}.ffn.fc2.bias'],    [ED])
    emit_float("W_NORM", sd['norm.w'],     [ED])
    emit_float("W_OUT",  sd['out.weight'], [VS, ED])

    # Vocab + merges.
    def hexdump(name, data, width=24):
        w(f"static const unsigned char {name}[{len(data)}] = {{\n")
        for i in range(0, len(data), width):
            chunk = data[i:i+width]
            w("    " + ", ".join(f"0x{b:02x}" for b in chunk) + ",\n")
        w("};\n\n")
    hexdump("VOCAB_STR_BLOB", bytes(blob))
    w("static const unsigned short VOCAB_OFFSETS[128] = {\n")
    for i in range(0, 128, 16):
        w("    " + ", ".join(f"{x:5d}" for x in offsets[i:i+16]) + ",\n")
    w("};\n\n")
    w("static const unsigned char VOCAB_LEN_TBL[128] = {\n")
    for i in range(0, 128, 16):
        w("    " + ", ".join(f"{x:3d}" for x in lengths[i:i+16]) + ",\n")
    w("};\n\n")
    w(f"static const unsigned char MERGES_AB[{len(merges)}][2] = {{\n")
    for a, b, _ in merges:
        w(f"    {{ {a}, {b} }},\n")
    w("};\n\n")
    w(f"static const unsigned char MERGES_ID[{len(merges)}] = {{\n")
    for i in range(0, len(merges), 16):
        w("    " + ", ".join(f"{m[2]:3d}" for m in merges[i:i+16]) + ",\n")
    w("};\n\n")
    w("#endif /* SOUL_DATA_FLOAT_H */\n")

    out_path.write_text("".join(out))
    print(f"wrote {out_path} — {out_path.stat().st_size} bytes")


if __name__ == '__main__':
    main()
