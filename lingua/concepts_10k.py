"""Tier-10K concept additions — procedural domain expansion.

This file lives beside ``lingua/concepts.py`` and contributes
``TIER_10K_ADDITIONS``. It is deliberately organized into visually
blocky sections so that future batches can be appended cleanly.

The ~744 concepts in ``TIER_FULL`` stop at semantic primitives.
``TIER_10K_ADDITIONS`` goes one level further into specific
kinds: tree species, mammal species, tool types, dish kinds.
At this scale the "cross-linguistically universal" claim no
longer holds in a strong sense — many of these concepts split
or merge in particular languages. Treat this tier as a broad
lexicon rather than a set of primes.

Naming conventions
──────────────────
- Concepts are ALL CAPS.
- Where a short name collides with a ``TIER_FULL`` entry (e.g.
  ``SPIDER`` is already in TIER_FULL as the generic arachnid),
  the species-level variant gets a suffix: ``SPIDER_TARANTULA``.
- ``_TREE`` / ``_FLOWER`` / ``_BIRD`` etc. suffixes disambiguate
  where a plain name would collide with a quality or verb
  (e.g. ``OAK_TREE``, ``ROSE_FLOWER``).
"""

