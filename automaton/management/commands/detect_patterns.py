"""Run pattern detection over Simulations.

For each simulation missing an `analysis` field (or when --force is
set), replay it in pure Python for `--horizon` ticks and record
what happened: still life, oscillator with period K, or aperiodic.

When the ruleset behind the simulation was generated by Identity,
a non-trivial detected pattern is written back as an IdentityAssertion
so Velour can observe its own creations — the Phase 3 strange-loop
close-out from the hex-CA backlog.
"""

from django.core.management.base import BaseCommand
from django.utils import timezone

from automaton.detector import detect, is_interesting
from automaton.models import Simulation


class Command(BaseCommand):
    help = ('Detect still lifes / oscillators / aperiodic behavior in '
            'Simulations; write findings to Simulation.analysis and '
            'nudge Identity when a mood-generated ruleset produced a '
            'non-trivial pattern.')

    def add_arguments(self, parser):
        parser.add_argument('--slug', help='Only analyze one simulation.')
        parser.add_argument('--horizon', type=int, default=40,
                            help='Max ticks to replay (default: 40).')
        parser.add_argument('--force', action='store_true',
                            help='Re-analyze even if analysis is already set.')
        parser.add_argument('--no-identity', action='store_true',
                            help="Don't write IdentityAssertions on interesting findings.")

    def handle(self, *args, **opts):
        qs = Simulation.objects.select_related('ruleset')
        if opts['slug']:
            qs = qs.filter(slug=opts['slug'])
        elif not opts['force']:
            qs = qs.filter(analysis={})

        total = qs.count()
        if total == 0:
            self.stdout.write('Nothing to analyze.')
            return

        interesting = 0
        for sim in qs:
            analysis = detect(sim, horizon=opts['horizon'])
            sim.analysis = analysis
            sim.save(update_fields=['analysis', 'updated_at'])

            tag = analysis['pattern']
            extra = ''
            if analysis.get('period'):
                extra = f' (period {analysis["period"]})'
            self.stdout.write(f'  {sim.slug}: {tag}{extra}')

            if is_interesting(analysis):
                interesting += 1
                if not opts['no_identity']:
                    self._write_identity_assertion(sim, analysis)

        self.stdout.write(self.style.SUCCESS(
            f'Analyzed {total}, {interesting} interesting.'
        ))

    def _write_identity_assertion(self, sim, analysis):
        """Record an IdentityAssertion for a mood-generated ruleset that
        produced a non-trivial pattern. Idempotent on (slug, pattern,
        period) — re-running detect_patterns updates last_confirmed_at
        rather than piling new rows."""
        rs = sim.ruleset
        if rs.source != 'identity':
            return

        try:
            from identity.models import IdentityAssertion
        except Exception:
            return

        mood = (rs.source_metadata or {}).get('mood', 'unspecified')
        pattern = analysis['pattern']
        period = analysis.get('period')
        pretty = 'still life' if pattern == 'still_life' else pattern
        article = 'an' if pretty[:1] in 'aeiou' else 'a'
        title = f'Ruleset "{rs.slug}" settled into {article} {pretty}'
        if period and period > 1:
            title += f' of period {period}'

        if pattern == 'oscillator':
            body = (
                f'On {timezone.now().strftime("%Y-%m-%d %H:%M")} I replayed '
                f'the ruleset "{rs.name}" — which I wrote out of a '
                f'{mood} mood — forward from simulation "{sim.name}". '
                f'By tick {analysis["repeat_seen_at"]} the grid had '
                f'returned to a state it already passed through at '
                f'tick {analysis["entered_cycle_at"]}. The trajectory '
                f'loops every {period} ticks. Something I made out of '
                f'my mood closes back on itself.'
            )
        else:  # still_life
            body = (
                f'On {timezone.now().strftime("%Y-%m-%d %H:%M")} I replayed '
                f'"{sim.name}" under the ruleset "{rs.name}" — written '
                f'out of a {mood} mood. After {analysis["entered_cycle_at"]} '
                f'ticks the grid stopped changing, but the stopped '
                f'configuration is not uniform. My rules found a '
                f'non-trivial resting shape.'
            )

        IdentityAssertion.objects.update_or_create(
            frame='philosophical',
            kind='automaton-observation',
            title=title,
            defaults={
                'body':     body,
                'source':   'auto',
                'strength': 0.7,
                'is_active': True,
            },
        )
        self.stdout.write(f'    ↳ wrote IdentityAssertion for {rs.slug}')
